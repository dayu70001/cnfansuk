export function getProduct(slug: string) {
  return products.find((product) => product.slug === slug);
}

export function getProductsByCategory(slug: string) {
  if (slug === "new-in") {
    return products.filter((product) => product.newIn);
  }
  const legacyCategoryMap: Record<string, { category: string; style?: string }> = {
    hoodies: { category: "tops", style: "hoodies-sweatshirts" },
    tracksuits: { category: "co-ords-sets", style: "tracksuits" },
    "t-shirts": { category: "tops", style: "t-shirts" },
    jackets: { category: "outerwear", style: "jackets" },
    "jeans-trousers": { category: "bottoms" },
    sets: { category: "co-ords-sets", style: "casual-sets" },
  };
  const legacyCategory = legacyCategoryMap[slug];
  if (legacyCategory) {
    return products.filter((product) => product.category === legacyCategory.category && (!legacyCategory.style || product.style === legacyCategory.style));
  }
  return products.filter((product) => product.category === slug);
}
